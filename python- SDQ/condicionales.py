# ------------Condicionales
# condicional if elif else

numero = int(input("Inserte un numero: "))

if numero > 10:
    print("El numero es mayor que 10")
elif numero <= 10:
    print("El numero es menor  o igual a 10")
elif numero <= 5:
    print("El numero es menor o igual a 5")
else:
    print("El numero es menor que 5")
